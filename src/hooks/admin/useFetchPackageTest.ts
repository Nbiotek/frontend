import { useCallback } from 'react';
import { superAdmin } from './FetchKeyFactory';
import { useQuery } from '@tanstack/react-query';

const select = (res: INBTServerResp<Array<TAdminTestItem>>) => res.data;

export function useFetchPackageTest(): IQueryHookResponse<Array<TAdminTestItem> | undefined> {
  const meta = superAdmin.getPackageTest();
  const memoizedSelect = useCallback(select, []);

  const { data, isLoading, status, error } = useQuery({
    queryKey: meta.keys(),
    meta,
    select: memoizedSelect
  });

  return { data, isLoading, status, error };
}
